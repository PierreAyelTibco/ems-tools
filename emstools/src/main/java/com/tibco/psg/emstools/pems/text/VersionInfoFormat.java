/**
 * <p> 
 * @author Pierre Ayel
 * @version 1.4.0
 */
public class VersionInfoFormat {

	/*************************************************************************/
	/***  CONSTRUCTORS  ******************************************************/
	/*************************************************************************/

	/**
	 * Private constructor.
	 * <p>
	 * @since 1.4.0
	 */
	private VersionInfoFormat() {
	}
			
	/*************************************************************************/
	/***  STATIC METHODS  ****************************************************/
	/*************************************************************************/

	public static String toCSV(final VersionInfo p_version) {
		if (null==p_version)
			return "";
		
		return p_version.toString();
	}
}

