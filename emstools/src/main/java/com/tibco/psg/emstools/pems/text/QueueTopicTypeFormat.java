public class QueueTopicTypeFormat {

	/*************************************************************************/
	/***  STATIC METHODS  ****************************************************/
	/*************************************************************************/

	public static String toCSV(int p_type) {
		switch(p_type) {
			case 1: return "QUEUE";
			case 2: return "TOPIC";
			default:
				return "ALL";
		}
	}
}

